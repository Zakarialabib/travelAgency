POURQUOI VOYAGER

L’apprentissage

Voulez-vous apprendre de nouvelles choses sur le monde et les cultures? Faire de nouvelles découvertes? vivre des expériences enrichissantes?

Venez expérimenter le mode vie nomade avec ses challenges et ses découvertes


Le challenge

C’est l’occasion de vous challenger, de vous lancer de nouveaux défis. De vous confronter à vos peurs pour devenir une meilleure version de vous-même. Venez expérimenter la sensation du challenge et défis.. Le sentiment de liberté et de dépassement de soi.. Faites l’expérience de sortir de votre zone de confort..


La liberté

Il n’y a rien de plus jouissif que de pouvoir vivre cette impression de liberté, de pouvoir faire ce qu’on veut quand on veut, libéré des contraintes du quotidien, libre de pouvoir enfin prendre son temps et vivre à son rythme.

Les rencontres

Les rencontres sont une part importante du voyage. Les personnes croisées sur la route sont d’une telle diversité ! Des nationalités, des religions, des conditions de vie, des milieux différents ! Une telle diversité nous apporte une vision différente des choses, de la vie, du monde. Cette vision permettra de compléter et parfaire celle que vos parents, votre milieu ou même votre pays vous ont transmise.

La nature

Les arbres, les plantes et les animaux. Mais je veux aussi parler de votre nature à vous; Votre rythme et vos instincts naturels.
Nous avons tous un rythme de vie différent, un rythme biologique défini depuis notre naissance, l’alternance des phases d’éveil et de sommeil que suit votre corps.  Or les contraintes de la vie quotidienne ne nous permettent pas de respecter ce rythme.
Mais en voyage, on peut enfin vivre selon notre rythme naturel. Et c’est ça qui fait un bien fou ! En voyage, je ne dors pas toujours très longtemps et rarement dans un bon lit ! Et pourtant, je suis toujours en pleine forme car je peux laisser mon corps suivre son rythme de sommeil !
Autre chose; A l’origine, notre cerveau servait à faire fonctionner nos instincts naturels : Manger, boire, dormir, se protéger (et éventuellement se reproduire, mais c’est un autre sujet).
Il n’était pas fait pour traiter ces problèmes de la vie quotidienne et ces informations qui nous submergent la tête. Bien sûr, nous avons su nous adapter. Mais ça fait du bien de renouer avec ses instincts. Et il n’y a rien de mieux pour ça que le voyage !

La découverte de soi

Premièrement, voyager permet de se découvrir car nous sommes confrontés à des situations inhabituelles, qui font ressortir certains cotés inconnus de notre personnalité.
Et deuxièmement, voyager permet de se découvrir car nous pouvons enfin être nous-même, et personne d’autre. Alors qu’à la maison, nous sommes contraints de suivre les conventions et les habitudes de la famille, des amis, du travail et de la société… Même inconsciemment, nous nous efforçons de nous fondre dans notre environnement. Mais, en voyage, toutes ces contraintes disparaissent ! C’est d’ailleurs l’une des raisons pour lesquelles je préfère partir seul en voyage.



